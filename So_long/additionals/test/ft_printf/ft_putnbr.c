#include "ft_printf.h"

int	ft_putnbr(int n)
{
	long int	ln;
	int			i;

	i = 0;
	ln = n;
	if (ln < 0)
	{
		i += ft_putchar('-');
		ln *= -1;
	}
	if (ln >= 10)
		i += ft_putnbr(ln / 10);
	i += ft_putchar(ln % 10 + '0');
	return (i);
}
